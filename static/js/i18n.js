    t(key) {
        const dict = this.translations[this.lang] || this.translations.zh;
        return dict[key] || key;
    },

    setLang(lang) {
        this.lang = lang;
        localStorage.setItem("lang", lang);
        // Update nav labels
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            el.textContent = this.t(key);
        });
        // Update dark mode label
        const darkLabel = document.getElementById("dark-label");
        if (darkLabel) {
            darkLabel.textContent = document.body.classList.contains("dark") ? this.t("light.mode") : this.t("dark.mode");
        }
        // Update lang switch label
        const langLabel = document.getElementById("lang-label");
        if (langLabel) {
            langLabel.textContent = this.lang === "zh" ? "EN" : "中文";
        }
    },

    toggle() {
        this.setLang(this.lang === "zh" ? "en" : "zh");
        // Reload current page to apply translations
        App.loadPage(App.currentPage);
    }
